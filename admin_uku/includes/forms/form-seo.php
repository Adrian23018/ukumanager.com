						<h2>Una Guía de SEO Paso a Paso</h2>
						<h4>Posicionamiento en Google a través de las Palabras clave</h4>
						<ul>
							<li>Elige bien tus palabras clave: define para qué búsquedas que hace la gente vas a intentar posicionar. Ten en cuenta el número de búsquedas de cada término para encontrar tus mejores palabras clave.</li>
							<li>Mide la densidad de las palabras: Analiza tu página y descubre qué palabras estás utilizando más. Intenta repetir más aquellas que quieras posicionar bien. Usa esta herramienta de <a href="http://www.internetmarketingninjas.com/seo-tools/keyword-density/" target="_blank">Internet Ninja</a></li>
							<li>Usa las palabras clave: Utilízalas en tus textos, etiquetas y categorías. Las 200 primeras palabras de cada página tienen más valor, así que es buena idea hacer una entradilla resumiendo el artículo. A la vez de  incentivar al resto de la lectura, aprovechas para posicionar tus palabras.</li>
							<li>Palabras clave en títulos: Si puedes utilízalas para crear títulos y de paso para basarte en ellas para tus contenidos.</li>
							<li>Mide y analiza tu posicionamiento natural para distintas palabras clave: Consulta tu clasificación en los buscadores para todas tus palabras clave. Puedes usar la siguiente herramienta gratuita para instalar en tu ordenador:  <a href="http://www.iblogzone.com/go/cuterank-2/" target="_blank">CuteRank</a> Te dirá el número de página en el que sales en Google para cada búsqueda.</li>
						</ul>
						<h4>Configuración del Sitio según el Manual SEO</h4>
						<ul>
							<li>Meta description y title: Define para cada página de tu web una descripcion y un título en las etiquetas Meta de cada encabezado<head>. Estos serán los títulos y descripciones que se vean en Google y con los que pretendes atraer la atención.</li>
							<li>Url amigables: Utiliza un sistema de escritura de URL que sean como tus títulos.</li>
							<li>Creación del sitemap: Los sitemaps ayudan a los buscadores a indexar tu sitio y tenerlos actualizados en su cache.</li>
							<li>Automatiza el envío de sitemaps: Se debe generar el sitemap cada vez que haya un cambio.</li>
							<li>Favicon: El favicon es el pequeño logo que sale en cada pestaña y en favoritos e identifica a cada web. </li>
						</ul>
						<h4>Las imágenes en el posicionamiento en buscadores</h4>
						<ul>
							<li>Título y descripción: Incluye en todas las Imágenes las etiquetas ALT y TITLE. Si no los buscadores no sabrán lo que son y no podrá indexarlas (recuerda que el nombre de la imagen va a quedar con ALT y Title) </li>
							<li>Optimización de imágenes para la web: Utiliza un compresor o guarda para la Web en Photoshop. No es necesaria la máxima calidad de imagen para la web y las imágenes ocupan gran parte de datos de descarga. Queremos que la página cargue lo antes posible. </li>
						</ul>
						<h4>Cuida los enlaces para un buen posicionamiento web</h4>
						<ul>
							<li>Anchor text diversificados: Estos son las palabras en las que pueden hacer click para visitar tu web. Debemos tratar de que nos enlacen de manera descriptiva, usando palabras clave y no siempre igual, sino variándolas, haciéndolas parecer como recomendaciones naturales.</li>
							<li>Comprueba los links rotos: Google nos odia si le hacemos visitar enlaces que no funcionan. Debemos evitarlo y comprobar todos nuestro enlaces habitualmente, todas las semanas. Con esta herramienta online es muy sencillo y nos dirá cuales no funcionan: <a href="http://www.internetmarketingninjas.com/seo-tools/google-sitemap-generator/" target="_blank">InternetMarketingNinjas&nbsp;</a> </li>
							<li>Evita las redirecciones: Tanto en nuestro sitio como a la hora de enlazar otras páginas. La herramienta anterior también nos dirá que enlaces tienen que redirigirse a otra dirección y nos sugiere que acortemos los paso</li>
							<li>No enlaces contenido malicioso o ilegal: Google penaliza a las páginas que enlazan contenidos con virus o declarados pirata.</li>
							<li>Busca en donde enlazan a tu competencia: Aprende de su estrategia a la vez que encuentras nuevas maneras de conseguir enlaces y mejorar el posicionamiento.</li>
							<li>Utiliza enlaces internos: Busca donde destacar otras partes de tu web y acostúmbrate a enlazar de manera natural tus artículos entre sí.</li>
						</ul>
						<h4>Evitamos el Contenido duplicado</h4>
						<ul>
							<li>Textos originales: Comprueba que todos tus textos son originales. Si no es así Google será el primero en saberlo y puntuará negativamente tu página. Puedes usar <a href="http://www.copyscape.com/" target="_blank">Copyscape</a>.</li>
							<li>Página inicial con sólo muestra: No incluiremos el texto completo de los artículos en la página inicial de nuestro blog, sólo una parte de muestra y un enlace de Leer Más. Sino Google va a tener duplicado los mismos textos y el artículo por si solo ya no va a tener contenido exclusivo.</li>
							<li>Descripción y Título Meta sin repetir: En las descripciones para los buscadores tampoco podemos tener ninguna repetida porque crearía otro conflicto a los buscadores.</li>
							<li>URL Canónica: Comprobar que tu dirección web es canónica: http://pagina.com y http://www.pagina.com</li>
							<li>Un mismo diseño para web y móvil  Es preferible usar un diseño adaptable o Responsive que nos permita tener el mismo diseño para todos los dispositivos. Si tenemos dos versiones distintas, los buscadores encontrarán contenido duplicado y tendrán que tener dos copias paralelas del sitio.</li>
						</ul>
						<h4>Tu Guía de Estilo del posicionamiento web</h4>
						<ul>
							<li>Crea contenidos divertidos y originales:  Haz que estos hagan al lector estar más tiempo en tu página y ver otras páginas del sitio. Cada vez Google tiene más en cuenta los factores de usabilidad y permanencia en tu web, así que el mayor truco es ser estupendos y dignos de mención. Estudia los tiempos y la tasa de rebote de cada artículo para prender lo que prefieren tus lectores.</li>
							<li>Contenidos largos: Si tu página son 4 párrafos el lector no durará mucho más de 20 segundos. Trata de hacer contenidos más ambiciosos y conseguirás retener mayor tiempo al lector. Eso si, sin pasarte, porque podrías caer en el error contrario y aumentar notablemente el tiempo de carga de la página.</li>
							<li>Cuida a tus visitantes desde dispositivos: Las visitas desde móviles y tablets son parte importante de las estadísticas y tienes que cuidar la usabilidad y la presentación de tu página para estas resoluciones. Si quieres que estén más tiempo en tu página deberás tener un diseño adaptable o en su defecto una versión de la web móvil. </li>
							<li>Publica periódicamente: Los buscadores valoran que tu web este actualizada y viva. Trata de publicar al menos semanalmente para ser catalogado como una fuente activa de información. Si tu web es estática, trata de crear un apartado de blog que puedas publicar con más frecuencia y crea Landing Pages para atraer diferentes tipos de búsquedas en buscadores</li>
							<li>Participa y conecta con tu comunidad: Será la mejor manera de que te enlacen y te mencionen. Ayuda a otros y resuelve las preguntas de los comentarios. Interactua y participa con aquella comunidad interesada en tus contenidos.</li>
							<li>Protocolo después de cada artículo: Después de publicar cada artículo ejecuta un protocolo de acciones que vayas perfeccionando con el tiempo. Este debería llevarte unos 20 minutos y es muy importante hacerlo después de publicar cada artículo o página. Podríamos publicar en Twitter, Facebook,  Pinterest, G+, Grupos de Linkedin, foros en los que participes, comentarios en artículos relacionados, directorios, agregadores de favoritos… Todo el trabajo inicial es bueno para conseguir algunos primeros enlaces en webs y compartidos en redes sociales. Tus artículos siempre son más visibles en los buscadores los 4 primeros días, así que el empujón inicial ayuda a aumentar las visitas en esos mejores días.</li>
							<li>Landing Page: Hacer un trabajo SEO específico para las diferentes Landing Page que puedes crear para buscar distintas maneras de atraer a la gente a tu web. Posiciona contenidos para determinadas palabras clave y monitorea las visitas que recibes a esa página a través de buscadores.</li>
							<li>Cuida al lector: Quita por ejemplo esa publicidad innecesaria que no te está dando ningún dinero de tu web. Ahorrarás tiempo de carga y mejorará la usabilidad de tu página.</li>
						</ul>
						<h4>Evita las penalizaciones de Google</h4>
						<ul>
							<li>No poner palabras clave fuera de contexto: No utilizar una técnica de poner palabras clave masivamente en texto sin ningún sentido para aumentar la densidad o recibir visitas.</li>
							<li>No poner texto escondido: Google penaliza fuertemente la técnica de escribir texto que el lector no ve, debajo de imágenes, vídeos u otras capas, con el fin de posicionar ciertos términos.</li>
							<li>Evita los errores de código que puedas: Los buscadores prefieren aquellas páginas con un código que cumple los estándares y se verá correcto para la mayoría de situaciones. La mejor manera que tenemos de saber cómo de correcto es nuestro código, es usar la herramienta de <a href="http://validator.w3.org/" target="_blank">W3C Markup Validator</a></li>
							<li>No te pases con el intercambio de enlaces: No está mal tener algunos (entre 5 y 20) enlaces de intercambio con otras páginas, siempre que sean de un tema muy afín al nuestro y haya participación real entre las dos. Desaconsejo crear grandes colecciones de enlaces intercambiados con otras webs de varios temas o especialidades distintas. Los mejores de todos serán los que ganes porque los mereces.</li>
						</ul>
						<h4>Reduce el tiempo de carga de tu página y Google te querrá mucho</h4>
						<ul>
							<li>Mide y mejora la velocidad de tu página: Es otro de los factores más importantes para un buen posicionamiento y podemos usar varias herramientas para medirlo. Con <a href="https://developers.google.com/speed/pagespeed/" target="_blank">Google Page Speed</a> nos dirá muchos puntos donde podemos mejorar nuestra velocidad</li>
							<li>No abusar de los códigos en javascript: Revisa cuantos javascript utiliza tu página y trata de ahorra en los que no sean muy necesarios. Nos gusta meterle muchas funciones y efectos a las webs, sin tener en cuenta del peso que eso conlleva.</li>
							<li>Ahorra y limpia tu código: Tú código no es lo que más ocupa, pero se abre muchas veces. Puedes limpiar tus archivos html, css y php de anotaciones innecesarias y comprimirlos con la ayuda de alguna herramienta.</li>
							<li>Comprueba los tiempos de carga de cada página: Analiza los tiempos de carga y su rebote, y prueba y mide distintas alternativas para mejorar la usabilidad. Puedes usar de nuevo <a href="http://gtmetrix.com/" target="_blank">GtMetrix</a>.</li>
						</ul>
